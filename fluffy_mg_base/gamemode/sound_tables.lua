-- Get a random death sound from the table
-- Has rare/uncommon/common sounds with different chances
function GM:GetRandomDeathSound(gender)
    if not gender then gender = 'male' end
    if gender != 'male' and gender != 'female' then gender = 'male' end
    
    local c = math.random()
    if c > 0.9 then
        -- Play a rare sound
        return table.Random(GAMEMODE.DefaultDeathSoundsRare[gender])
    elseif c > 0.7 then
        -- Play an uncommon sound
        return table.Random(GAMEMODE.DefaultDeathSoundsUncommon[gender])
    else
        -- Play a common sound
        return table.Random(GAMEMODE.DefaultDeathSoundsCommon[gender])
    end
end

-- Determine the gender of a given playermodel
function GM:DetermineModelGender(model)
    if string.find(model, 'female') then
        return 'female'
    else
        return 'male' -- default to male because of the patriarchy
    end
end